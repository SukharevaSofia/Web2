public class dataBean {
    public Integer dataX;
    public Integer dataY;
    public Integer dataR;
    public Boolean result;
    public String currentTime;
    public String workingTime;

    public dataBean(){}

    public Integer getDataX(){
        return dataX;
    }

    public void setDataX(Integer dataX) {
        this.dataX = dataX;
    }

    public Integer getDataY() {
        return dataY;
    }

    public void setDataY(Integer dataY) {
        this.dataY = dataY;
    }

    public Integer getDataR() {
        return dataR;
    }

    public void setDataR(Integer dataR) {
        this.dataR = dataR;
    }

    public Boolean getResult() {
        return result;
    }

    public String getCurrentTime() {
        return currentTime;
    }

    public String getWorkingTime() {
        return workingTime;
    }
}
